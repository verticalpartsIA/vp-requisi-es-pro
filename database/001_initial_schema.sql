create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  department text,
  email text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.user_roles (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  role public.app_role not null,
  created_at timestamptz not null default now(),
  unique (user_id, role)
);

create table if not exists public.requisitions (
  id uuid primary key default gen_random_uuid(),
  ticket_sequence bigint generated by default as identity,
  ticket_number text unique,
  module public.requisition_module not null,
  title text not null,
  description text not null,
  justification text not null,
  urgency public.requisition_urgency not null default 'MEDIUM',
  status public.requisition_status not null default 'ABERTO',
  estimated_cost numeric(12, 2),
  desired_date date,
  requester_profile_id uuid references public.profiles(id) on delete set null,
  requester_name text not null,
  requester_email text,
  requester_department text,
  created_by uuid references auth.users(id) on delete set null,
  completed_at timestamptz,
  module_data jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.quotations (
