create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create or replace function public.generate_ticket_number()
returns trigger
language plpgsql
as $$
declare
  sequence_number bigint;
begin
  if new.ticket_number is not null then
    return new;
  end if;

  sequence_number := coalesce(new.ticket_sequence, nextval(pg_get_serial_sequence('public.requisitions', 'ticket_sequence')));
  new.ticket_sequence := sequence_number;
  new.ticket_number := new.module::text || '-' || lpad(sequence_number::text, 6, '0');
  return new;
end;
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, full_name, department, email)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'full_name', new.email),
    new.raw_user_meta_data ->> 'department',
    new.email
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

